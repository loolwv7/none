渔父


屈原既放，游於江潭，行吟泽畔，颜色憔悴，形容枯槁。渔父见而问之曰：
“子非三闾大夫与？何故至於斯！”
屈原曰：“举世皆浊我独清，众人皆醉我独醒，是以见放！”
渔父曰：“圣人不凝滞於物，而能与世推移。
世人皆浊，何不其泥而扬其波？
众人皆醉，何不J其糟而f其酾？
何故深思高举，自令放为？”
屈原曰：“吾闻之，新沐者必弹冠，新浴者必振衣；
安能以身之察察，受物之汶汶者乎！
宁赴湘流，葬於江鱼之腹中。
安能以皓皓之白，而蒙世俗之尘埃乎！”
渔父莞尔而笑，鼓ざ去，乃歌曰：
“沧浪之水清兮，可以濯吾缨。
沧浪之水浊兮，可以濯吾足。”
遂去不复与言。


